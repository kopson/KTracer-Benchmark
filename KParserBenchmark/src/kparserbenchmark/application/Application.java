package kparserbenchmark.application;

import java.io.File;
import java.io.IOException;
import java.util.logging.FileHandler;
import java.util.logging.Level;
import java.util.logging.Logger;

import kparserbenchmark.projectexplorer.Workspace;

import org.eclipse.equinox.app.IApplication;
import org.eclipse.equinox.app.IApplicationContext;
import org.eclipse.swt.widgets.Display;
import org.eclipse.ui.IWorkbench;
import org.eclipse.ui.PlatformUI;

/**
 * This class controls all aspects of the application's execution
 * 
 * @author kopson
 */
public class Application implements IApplication {

	// Logger instance
	private final static Logger LOG = Logger.getLogger(Application.class
			.getName());

	@Override
	public Object start(IApplicationContext context) {

		try {
			FileHandler fileHandler1 = new FileHandler("KLOG.log");
			LOG.addHandler(fileHandler1);
		} catch (IOException ioe) {
			LOG.log(Level.SEVERE, "Unable to create file handler.", ioe);
		}

		Workspace.getInstance().createDefaultWorkspace(
				System.getProperty("user.home") + File.separator
						+ Workspace.KWorkspace);

		Display display = PlatformUI.createDisplay();
		try {
			int returnCode = PlatformUI.createAndRunWorkbench(display,
					new ApplicationWorkbenchAdvisor());
			if (returnCode == PlatformUI.RETURN_RESTART) {
				return IApplication.EXIT_RESTART;
			}
			return IApplication.EXIT_OK;
		} finally {
			display.dispose();
		}
	}

	@Override
	public void stop() {
		if (!PlatformUI.isWorkbenchRunning())
			return;
		final IWorkbench workbench = PlatformUI.getWorkbench();
		final Display display = workbench.getDisplay();
		display.syncExec(new Runnable() {
			public void run() {
				if (!display.isDisposed())
					workbench.close();
			}
		});
	}
}
